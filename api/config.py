# -*- coding: utf-8 -*-
"""
config.py (api 模块专用)
与 embedding/config.py 是分开的两份配置：
embedding/ 那份是你本机跑索引构建用的，api/ 这份是给同组同学调用的服务配置。
这样即使其他同学的轻薄本上没有装 faiss、没有清洗好的数据，
也完全不影响她们通过接口调用你这边跑起来的模型服务。
"""

# ========== Ollama 模型配置 ==========
# 对话生成模型（第2周 RAG 用这个来生成回答）
CHAT_MODEL = "qwen2:7b"

# Ollama 服务地址。默认是本机的 11434 端口。
# 如果你把 Ollama 和 API 服务分开部署在不同机器上，改这里。
OLLAMA_HOST = "http://127.0.0.1:11434"

# 调用大模型的超时时间（秒）。qwen2:7b 在 CPU 上生成较长回答可能比较慢，
# 超时时间不要设太短，先给宽松一点，后面根据实测速度再调整。
GENERATE_TIMEOUT = 120

# ========== 性能相关配置 ==========
# 限制模型单次最多生成多少 token。不设的话模型有时候会展开讲很长，
# 拖慢响应时间，也让多人同时用的时候排队更久。500 大概对应几百字的中文回答，够用。
MAX_TOKENS = 500

# /chat 多轮对话时，只把最近 N 轮（一问一答算一轮）历史传给模型，
# 而不是把从头到尾的所有历史都传进去。
# 原因：历史越长，prompt 越长，生成速度越慢——对话轮数一多，
# 后面每一轮都会因为要重新处理前面所有内容而变慢。
# 注意：这只影响"这次发给模型看多少历史"，接口返回给调用方的 messages 仍然是完整历史，
# 不影响 B/C 那边保存的对话记录。
MAX_HISTORY_TURNS = 5

# 模型用完之后在显存里保持"热"的时长。默认 Ollama 是 5 分钟，
# 联调期间大家断断续续地测，5 分钟很容易超时导致模型被卸载、下一次请求又要重新加载（慢很多）。
# 调大到 30 分钟，联调这几天体验会更稳定。
KEEP_ALIVE = "30m"


# ========== FastAPI 服务配置 ==========
# 监听地址：0.0.0.0 表示局域网内其他同学的电脑也能通过你的 IP 访问，
# 如果只写 127.0.0.1，那就只有你自己电脑能访问，同组同学连不上。
HOST = "0.0.0.0"
PORT = 8000